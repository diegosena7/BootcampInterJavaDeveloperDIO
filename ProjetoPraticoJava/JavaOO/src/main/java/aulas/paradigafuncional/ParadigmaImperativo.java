package aulas.paradigafuncional;
/*
Usado o paradigma imperativo, quando damos ordens a JVM através das instruções do programa
 */
public class ParadigmaImperativo {
    public static void main(String[] args) {
        int valor = 10;//Instrução 1
        int resultado = valor * 3;//Instrução 2
        System.out.println("O resultado é: " + resultado);//Instrução 3
    }
}
